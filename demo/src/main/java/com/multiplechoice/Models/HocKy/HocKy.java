package com.multiplechoice.Models.HocKy;

public class HocKy {
	public String maHK;
	public String tenHK;
	public HocKy(String maHK, String tenHK) {
		super();
		this.maHK = maHK;
		this.tenHK = tenHK;
	}
	public String getMaHK() {
		return maHK;
	}
	public void setMaHK(String maHK) {
		this.maHK = maHK;
	}
	public String getTenHK() {
		return tenHK;
	}
	public void setTenHK(String tenHK) {
		this.tenHK = tenHK;
	}
	
}
